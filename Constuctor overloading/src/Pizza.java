
public class Pizza {
	
	String bread;
	String sauce;
	String cheese;
	String Topping;
	
	Pizza(String bread,String sauce){
		this.bread=bread;
		this.sauce=sauce;
	}
	
	Pizza(String bread,String sauce,String cheese){
		this.bread=bread;
		this.sauce=sauce;
		this.cheese=cheese;
	}
	
	Pizza(String bread,String sauce,String cheese,String Topping){
		this.bread=bread;
		this.sauce=sauce;
		this.cheese=cheese;
		this.Topping=Topping;
	}
}
